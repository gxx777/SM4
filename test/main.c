#include "sm4.h"

int main()
{
    unsigned char IN[16] = {0x01,0x23,0x45,0x67,0x89,0xab,0xcd,0xef,
                            0xfe,0xdc,0xba,0x98,0x76,0x54,0x32,0x10};
    unsigned char key[16] = {0x01,0x23,0x45,0x67,0x89,0xab,0xcd,0xef,
                            0xfe,0xdc,0xba,0x98,0x76,0x54,0x32,0x10};
    unsigned char OOUT[16];
    
    sm4_context ctxx;
    sm4_setkey_enc(&ctxx, key);
    sm4_crypt_ecb(&ctxx, 1, 16, IN, OOUT);
    for(int i=0;i<16;i++)
    {
        printf("%02X ",OOUT[i]);
    }
    printf("\n");
    return 0;
}